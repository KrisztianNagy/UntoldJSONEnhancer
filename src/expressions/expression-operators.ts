export class ExpressionOperators {
    static basicPlusOperator(left: any, right: any): any {
        return left + right;
    }

    static basicMinusOperator(left: any, right: any): any {
        return left - right;
    }

    static basicMultipleOperator(left: any, right: any): any {
        return left * right;
    }

    static basicDivideOperator(left: any, right: any): any {
        return left / right;
    }

    static basicModuloOperator(left: any, right: any): any {
        return left % right;
    }
}
